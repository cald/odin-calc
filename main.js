// Addition function
const addition = (a, b) => a + b;

// Subtraction function
const subtraction = (a, b) => a - b;

// Multiplication function
const multiplication = (a, b) => a * b;

// Division function
const division = (a, b) => a / b;

// Operate function
const operate = (operator, a, b) => operator(a, b);
